package com.example.demo_id.entities;

public class CashRegister {

    private int cashRegisterId;
    private int cashRegisterDate;
    
    public CashRegister() {
    }

    public int getCashRegisterId() {
        return cashRegisterId;
    }

    public void setCashRegisterId(int cashRegisterId) {
        this.cashRegisterId = cashRegisterId;
    }

    public int getCashRegisterDate() {
        return cashRegisterDate;
    }

    public void setCashRegisterDate(int cashRegisterDate) {
        this.cashRegisterDate = cashRegisterDate;
    }

}
